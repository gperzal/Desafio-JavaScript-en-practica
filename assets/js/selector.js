// declara la variable que consigue el elemento con ID btn-1
let btn_1 = document.getElementById("btn-1");
// activa la función cuando se hace click al elemento con ID anteriormente mencionado
btn_1.addEventListener("click", function () {
  // cuando se clickea el elemento con ID anteriormente mencionado, modifica la caja de la derecha
  document
    .getElementById("box-bg")
    .setAttribute(
      "style",
      "height: 100%; border: 1px solid black; background-color: #d14d41"
    );
});

